				<p class="otherp big clr2">Advaita Sharada Project</p>
				<p class="otherp">Over a century ago, the 33rd Jagadguru of Sringeri, Sri Sacchidananda Shivabhinava Narasimha Bharati Mahaswamiji led the first ever compilation of the complete works of Sri Adi Shankaracharya -- Sri Shaankara Granthavali. In keeping with this tradition and sensitive to our technology age, the presiding Shankaracharya of the Dakshinamnaya Sri Sharada Peetham, Jagadguru Shankaracharya Sri Sri Bharati Tirtha Mahaswamiji is blessing and guiding the Advaita Sharada Project. </p>
				<p class="otherp">The goal of the Advaita Sharada project is to make available our ancient Shastric texts (in particular Vedantic texts) for access via the Internet and other computer-based devices like tablets and smartphones. Leveraging audio, video, commentaries, sub-commentaries, notes, tags and hyperlinks, we hope to provide a platform for in-depth research and additional learning for seekers, scholars and students. We also plan to create a learning platform to introduce the Upanishadic philosophy of Advaita for beginners and early aspirants.</p>
				<p class="otherp">Invoking the blessings of Goddess Sharada (the presiding deity of Sringeri Sharada Peetham) and reflecting the Upanishadic teachings as expounded by Jagadguru Sri Adi Shankaracharya, the project has been named "Advaita Sharada".</p>
